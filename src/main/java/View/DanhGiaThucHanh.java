    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int columnIndex = 3;
        if(tbBangDanhGiaPhongThucHanh.getValueAt(0, columnIndex) == null || tbBangDanhGiaPhongThucHanh.getValueAt(1, columnIndex) == null ||
                tbBangDanhGiaPhongThucHanh.getValueAt(2, columnIndex) == null || tbBangDanhGiaPhongThucHanh.getValueAt(3, columnIndex) == null ||
                tbBangDanhGiaPhongThucHanh.getValueAt(4, columnIndex) == null){
            JOptionPane.showMessageDialog(null, "Các điểm đánh giá phải được đánh giá hết ạ!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ds.remove(x);
        x.setDiemSangLoc(Short.parseShort(tbBangDanhGiaPhongThucHanh.getValueAt(0, columnIndex).toString()));
        x.setDiemSapXep(Short.parseShort(tbBangDanhGiaPhongThucHanh.getValueAt(1, columnIndex).toString()));
        x.setDiemSachSe(Short.parseShort(tbBangDanhGiaPhongThucHanh.getValueAt(2, columnIndex).toString()));
        x.setDiemSanSoc(Short.parseShort(tbBangDanhGiaPhongThucHanh.getValueAt(3, columnIndex).toString()));
        x.setDiemSanSang(Short.parseShort(tbBangDanhGiaPhongThucHanh.getValueAt(4, columnIndex).toString()));
        ds.add(x);
        new FileData<Phong>().writeFile(ds, "Phong.txt");
        JOptionPane.showMessageDialog(null, "Đánh giá thành công!", "Thông báo", JOptionPane.INFORMATION_MESSAGE);
        new NhapPhongDanhGia().setVisible(true);
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

